// Gang-box location derivation (ADR 0021).
//
// A contained asset has no independent place. Its effective location is the
// current location of its one top-level container. This tiny shared helper is
// used by the asset page, dashboard, scanner, and transfer builder so that the
// answer cannot drift between field surfaces.

(function (root) {
  'use strict';

  function container(asset) {
    if (!asset || !asset.container_id) return null;
    return asset.expand && asset.expand.container_id || null;
  }

  function effectiveLocationId(asset) {
    if (!asset) return '';
    if (!asset.container_id) return asset.current_location || '';
    const box = container(asset);
    return box ? (box.current_location || '') : '';
  }

  function effectiveLocation(asset) {
    if (!asset) return null;
    if (!asset.container_id) {
      return asset.expand && asset.expand.current_location || null;
    }
    const box = container(asset);
    return box && box.expand && box.expand.current_location || null;
  }

  function membershipPeriods(events) {
    const ordered = Array.from(events || []).sort((a, b) =>
      String(a.created).localeCompare(String(b.created)));
    const periods = [];
    let open = null;
    for (const event of ordered) {
      if (event.action === 'added') {
        // A malformed double-add is never generated by TrenchNote, but close
        // the old interval honestly if imported data contains one.
        if (open) { open.end = event.created; periods.push(open); }
        open = { containerId: event.container_id, start: event.created,
          end: null, added: event, removed: null };
      } else if (event.action === 'removed' && open &&
                 open.containerId === event.container_id) {
        open.end = event.created;
        open.removed = event;
        periods.push(open);
        open = null;
      }
    }
    if (open) periods.push(open);
    return periods;
  }

  // Merge direct movements, membership transitions, and movements inherited
  // from a box while the asset belonged to it. Server `created` order is used
  // for membership and movement facts, matching ADR 0008 arrival-order truth.
  function locationHistory(directMoves, events, containerMoves) {
    const rows = [];
    for (const move of (directMoves || [])) {
      rows.push({ kind: 'move', created: move.created, move,
        from: move.expand && move.expand.from_location || null,
        to: move.expand && move.expand.to_location || null });
    }
    for (const event of (events || [])) {
      rows.push({ kind: event.action, created: event.created, event,
        container: event.expand && event.expand.container_id || null,
        to: event.expand && event.expand.location || null });
    }
    for (const period of membershipPeriods(events)) {
      for (const move of (containerMoves || [])) {
        if (move.asset !== period.containerId) continue;
        if (String(move.created) < String(period.start)) continue;
        if (period.end && String(move.created) >= String(period.end)) continue;
        rows.push({ kind: 'via_container', created: move.created, move,
          container: period.added.expand && period.added.expand.container_id || null,
          from: move.expand && move.expand.from_location || null,
          to: move.expand && move.expand.to_location || null });
      }
    }
    rows.sort((a, b) => String(b.created).localeCompare(String(a.created)));
    return rows;
  }

  root.TNContainers = {
    container,
    effectiveLocationId,
    effectiveLocation,
    membershipPeriods,
    locationHistory,
  };
})(window);
